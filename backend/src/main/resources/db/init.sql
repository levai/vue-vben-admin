-- 创建数据库（如果不存在）
CREATE DATABASE IF NOT EXISTS `vben_admin` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

USE `vben_admin`;

-- 设置会话字符集为 UTF-8（确保后续操作使用正确的字符集）
SET NAMES utf8mb4;
SET CHARACTER SET utf8mb4;

